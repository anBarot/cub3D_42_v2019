#include "libft.h"

int	ft_is_printf_option(char c)
{
	if (ft_is_printf_type(c) || c == '-' || c == '+' || c == ' ' || c == '*'
	|| ft_isdigit(c) || c == '#' || c == '.')
		return (1);
	return (0);
}
